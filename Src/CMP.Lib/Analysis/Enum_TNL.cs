namespace CMP.Lib.Analysis;

/// <summary>
/// Tree Node Location
/// </summary>
public enum TNL
{
    /// <summary>
    /// The node location is undefined
    /// </summary>
    Undefined = 0,

    /// <summary>
    /// The node is the root
    /// </summary>
    Root = 1,

    /// <summary>
    /// The node is a branch
    /// </summary>
    Branch = 2,
}
